		<p class=text><b>	During winter and spring, Robostorm Robotics worked on a lot of projects. Aquabot 2.0, is an improvement on last year’s water bottle dispensing robot, Aquabot. 
		Aquabot 2.0 will have a moneychanger, and when it retrieves money, it will determine what to do on its own by running through a flowchart.
Other projects were Java programming, HTML classes, and Arduino classes. The Arduino and Java classes were to prepare for Aquabot 2.0.<br><br>
	In November, the club helped out with cleaning up the trails at the Morales Nature Preserve Park. During December, Robostorm sold many crafts and utilized its 3-D printer to print out 4-H clovers.
At the fair this year, the club will have an Arcade-themed tent designed by its members and the whole club is in full swing getting it ready!
</b></p></div>
