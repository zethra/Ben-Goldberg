		<div class="CollapsiblePanelTab" tabindex="0"><center><h1 class=link>September, 2014 Robostorm Update written for National 4-H Week</font></h1><br><p class=link>Click For Details</p></center></div>
        <div class="CollapsiblePanelContent">
		<p class=text><b>	The members of the Robostorm 4-H Robotics Club continued to be involved in a variety of robotics and technology projects this past year, culminating in a phenomenal five-day interactive 
		science-fair-like experience at the summer’s County fair.  Each club member had an individual invention on display, which were explained during each day’s Tech Demo hour.  The range of skills and technologies 
		exercised was impressive; most designs were original.  There were robots using touch and ultrasonic sensors, Bluetooth and RF remote controlling, solar energy, a home built 3-D printer, CAD design used to 3-D 
		print, electrical wiring and soldering, a highly articulated robotic arm, a vehicle propelled under water, a homemade computer, a web site programmed so the user could remotely drive a robot, repurposed drill 
		batteries used to power a drivable robot.<br><br>
	A number of group projects resulted in interactive exhibits:  two homemade video arcade machines providing classic games like Pong and Pac Man, a space invaders game, and a pair of remote-controlled robots for 
fair-goers to try driving through a maze.  Two Battle Bots tournaments were held, a double-elimination contest where the winning robot pushed its opponent out of the arena.  The Bots were all original designs.  
You may have purchased a cold bottle of water from the remote-controlled traveling water bottle vendor, Aqua Bot.  It was built on the chassis of an old motorized wheel chair, used a vending machine bill feeder, an 
Arduino microcontroller, and contained 3-D printer-fabricated internal parts for dispensing the bottles.<br><br>
	In addition to their dedication to robotics projects, Robostorm members are also active in the 4-H community.  The club’s entry in the annual Pumpkin Junction scarecrow contest, “Bumblebee”, was last year’s winner.  
Many enjoyed the Halloween tailgate festivities.  Most of Robostorm’s members competed in the annual Public Presentations contest, and one continued to the state contest.  Our bakers made goodies for several 4-H 
sponsored events.  Robostorm’s club members held a successful Lego sales fundraiser at the fair, raising money to donate to two local charities.  The club also volunteers each year to help with trail maintenance at 
Raritan Township’s Morales Nature Preserve.  Robostorm is proud that several of its members pursue additional community service opportunities on their own.<br><br>
	Being a club of only 3-1/2 years, the Robostorm 4-H Robotics Club hopes to continue providing a creative outlet for Hunterdon STEM fans.  With the growing interest from Hunterdon families, as was apparent during the 
fair, we would be happy to assist anyone who is inspired and motivated to start another robotics club.
</b></p></div>
